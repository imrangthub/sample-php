<?php
/**
       asort()	Sorts an associative array in ascending order, according to the value
 */


$Numarr1=array(10,2,4,6,100);
asort($Numarr1);
print_r($Numarr1);
echo "</br>";

//  arsort()	Sorts an associative array in descending order, according to the value

$Numarr1=array(10,2,4,6,100);
arsort($Numarr1);
print_r($Numarr1);